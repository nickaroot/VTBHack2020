import UIKit

extension UIColor {
    static var VTBlightBlue: UIColor {
        return UIColor(red: 58 / 255, green: 131 / 255, blue: 241 / 255, alpha: 1)
    }
    
    static var VTBdarkBlue: UIColor {
        return UIColor(red: 0, green: 40 / 255, blue: 130 / 255, alpha: 1)
    }
}
